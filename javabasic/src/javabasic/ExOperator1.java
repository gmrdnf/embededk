package javabasic;

public class ExOperator1 {
	public static void main(String[] args) {
		
	//다양한 연산자들을 실습해 봅니다
	int i= 1;
//	부호연산자
	i=+1;
	i=-1;
//	비트연산자
//	쉬프트연산자
//	산술연산자
	i= i*2;
	i= i/2;
	i= i%2;
//	관계연산자
	if(i==0) {
		System.out.println(i);
	}
//	논리연산자
	System.out.println(!(i==0));
//	대입연산자
	System.out.println(i+=1);
//	형변환연산자
	
//	상속관계확인 연산자

	
	
	
	
	
	
	
	
	
	
	
	}//main
}//class
